package aula_3;

public class Exe1 {
	float numero1;
	float numero2;
	
	float adicao() {
		return numero1+numero2;
	}
	
	float subtracao() {
		return numero1-numero2;
	}
	
	float multiplicacao() {
		return numero1*numero2;
	}
	
	float divisao() {
		return numero1/numero2;
	}

}
